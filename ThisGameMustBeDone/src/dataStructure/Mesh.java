package dataStructure;

public class Mesh {

	private int meshID ;
	private int vertexCount ;
	
	public Mesh(int meshID, int vertexCount) {
		
		this.meshID = meshID;
		this.vertexCount = vertexCount;
	}

	public int getMeshID() {
		return meshID;
	}

	public int getVertexCount() {
		return vertexCount;
	}
	
	
	
	
}
